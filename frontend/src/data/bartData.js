// Helper to find lines serving a station
export const getLinesForStation = (stationAbbr) => {
    const lines = [];
    for (const lineKey in bartRoutes) {
        if (bartRoutes[lineKey].stations.includes(stationAbbr)) {
            lines.push(lineKey);
        }
    }
    return lines;
};

// Helper to find the route details (stations, color) for a specific line key (e.g., "YELLOW-S")
